package chapter04;

public class StringTest01 {
	public static void main(String[] args) {
		// c:\temp
		System.out.println("c:\\temp"); // c:\temp

		// "Hello"
		System.out.println("\"Hello\""); // "Hello"

		// Escape Character
		// \t: tab
		// \r: carriage return
		// \n: newline
		// \b: beep
		System.out.print("hello\tworld\n");
		System.out.println("hello\tworld");

		// '
		char c = '\'';
		// 코드 추가
	}
}
